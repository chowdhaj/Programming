package mikeDaneJavaTutorial.tutorial26;

/*
 * Student class that contains the specification for
 * what a Student is and what attributes it has. Currently,
 * a Student is defined by first name, last name, major, gpa, 
 * age, and whether they are on academic probation or not.
 */
public class Student {
	
	String firstName;
	String lastName;
	String major;
	
	double gpa;
	int age;
	
	boolean onProbation;
}
