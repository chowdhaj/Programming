package lesson.examples.playingcardsexample; // Location of the 'Suits' class file

/**
 * The 4 Suits Of Playing Cards [Clubs, Diamonds, Hearts, Spades]
 */

public class Suits {
	
	// Card Suits are stored in integer variables ranging from 0 - 3
	
    public static final int CLUBS = 0;
    public static final int DIAMONDS = 1;
    public static final int HEARTS = 2;
    public static final int SPADES = 3;
}

/*

public = Can be accessible outside the class

static = Does not need to be instantiated to be used

final = Constant and cannot be changed

int = integer = variable type 


*/
