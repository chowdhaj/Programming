package lesson.examples.playingcardsexample; // Location of this class file

/**
 * Card Ranks Of Playing Cards From Two - Ace
 */

public class Ranks {
	
	// Card Ranks are stored in integer variables ranging from 0 - 12
	
    public static final int TWO = 0;
    public static final int THREE = 1;
    public static final int FOUR = 2;
    public static final int FIVE = 3;
    public static final int SIX = 4;
    public static final int SEVEN = 5;
    public static final int EIGHT = 6;
    public static final int NINE = 7;
    public static final int TEN = 8;
    public static final int JACK = 9;
    public static final int QUEEN = 10;
    public static final int KING = 11;
    public static final int ACE = 12;
}

/*

public = Can be accessible outside the class

static = Does not need to be instantiated to be used

final = Constant and cannot be changed

int = integer = variable type 

*/
